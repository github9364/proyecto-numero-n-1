#include<stdio.h>

int main()
{
	int a, b, c, c1;
	

	
	printf("Este programa suma, resta \n");
	
	printf("Ingresa el primer numero : \n");
	scanf("%d",&a);
	
	printf("Ingresa el segundo numero : \n");
	scanf("%d",&b);
	
	c=a+b;
	c1=a-b;
	
	


	printf("\n El resultado de la suma es: %d ",c);
	printf("\n El resultado de la resta es: %d ",c1);

	return 0;
}
